# 자료형(문자열,내장함수)

# 문자열(String)은 숫자를 제외한 모든 문자, 단어

# 표기:한 줄 문자열: "~", '~'
# 여러 줄:"""~~            또는 '''~~
#         ~~~"""                ~~~''' 

# 이스케이프 문자(코드)
# \n 다음줄로이동 (enter)
# \r 줄에서 처음으로 이동후 글자수만큼지우고 출력
# \t 8칸 공백 문자를쓰면 사실상 8-문자수만큼 뛰어진다
# \' = ', \" = " ,\\ = \

print('hello\nworld')
print('|탭\t|공백')
print('hello\rworld!')
print('역슬래쉬\\')
print('\'작은\'따옴표')
print("'작은'따옴표")
