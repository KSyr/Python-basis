#숫자형(자료)

#정수형   [정수 출력]
print(43,-185,0)

#실수형   [실수 출력]
print(1.256,-1.238)

#정수형,실수형
print(4*7,100/5)   #,가 스페이스 기능도 갖고 있음

#숫자형(진법)

print(0b100)    #2진법 -> 십진수
print(0o100)    #8진법 -> 십진수
print(100)      #10진법 -> 십진수
print(0x100)    #16진법 -> 십진수

#진법함수
print(bin(100))
print(oct(100))
print(hex(100))

#진법 예시
print(hex(3452+5785)) #16진법
print(0b100+0o652)    #10진법
print(bin(345+0o64))  #2진법
